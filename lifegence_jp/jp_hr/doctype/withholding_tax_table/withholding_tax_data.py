def get_withholding_tax(monthly_salary, dependents=0, table_type="甲"):
	"""Look up monthly withholding tax amount.

	Args:
		monthly_salary: Monthly salary amount (yen)
		dependents: Number of dependents (0-7, for 甲欄 only)
		table_type: "甲" (Class A - with exemption declaration) or "乙" (Class B - without)

	Returns:
		Withholding tax amount (int, yen)
	"""
	monthly_salary = int(monthly_salary)
	dependents = min(int(dependents), 7)

	if table_type == "乙":
		# 乙欄 lookup
		for salary_from, salary_to, tax in WITHHOLDING_TAX_TABLE_MONTHLY_OTSU:
			if salary_from <= monthly_salary < salary_to:
				return tax
		# Above table range: use formula (salary * 20.42% for > 710,000)
		if monthly_salary >= 710000:
			return int(monthly_salary * 0.2042)
		return 0

	# 甲欄 lookup
	for row in WITHHOLDING_TAX_TABLE_MONTHLY_KOU:
		salary_from, salary_to = row[0], row[1]
		if salary_from <= monthly_salary < salary_to:
			# dep_0 is index 2, dep_1 is index 3, etc.
			dep_index = min(dependents, 7) + 2
			if dep_index < len(row):
				return row[dep_index]
			return 0

	# Above table range: use formula
	if monthly_salary >= 710000:
		# Simplified: use approximate rate for high earners
		base_tax = int((monthly_salary - 710000) * 0.3321 + 45000)
		# Reduce per dependent
		dep_reduction = min(dependents, 7) * 1580
		return max(0, base_tax - dep_reduction)

	return 0
